<script>
export default {
  // 组件名，如果没有配置name，才会找文件名作为组件名
  name: "LayoutPage",

  // 一旦组件被缓存了，就不会执行created,mounted,destroyed等钩子了
  // 所以提供了 actived 和 deactived
  created(){
    console.log('created 组件被加载了');
  },
  mounted(){
    console.log('mounted dom渲染完了');
  },
  destroyed(){
    console.log('destroyed 组件被销毁了');
  },
  
  // 所以后续需要传参时，就可以使用下面2个钩子
  actived(){
    alert(`你好，欢迎回到首页`)
    console.log('actived 组件被激活了，看到页面了');
  },
  deactived(){
    console.log('deactived 组件失活了，离开页面了');
  },

}
</script>

